    private void jButtonAgregarMateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarMateActionPerformed
        // TODO add your handling code here:

        Materia mate = new Materia();

        mate.setAnio(Integer.parseInt(jTextFieldAnio.getText()));
        mate.setNombre(jTextFieldNombre.getText());
        mate.setEstado(jRadioButtonEstado.isSelected());

        try{
            matedata.AgregarMateria(mate);
        }catch(NullPointerException npe){

            JOptionPane.showMessageDialog(this, "Faltan datos para crear la materia");
        }
    }//GEN-LAST:event_jButtonAgregarMateActionPerformed


    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // TODO add your handling code here:

        Materia materia = null;
        try{
            if (jTextFieldCodigo.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ingrese un ID");

            }else{

                int id = Integer.parseInt(jTextFieldCodigo.getText());
                materia = matedata.buscarMateriaPorID(id);
            }

            jTextFieldCodigo.setText(String.valueOf(materia.getIdMateria()));
            jTextFieldAnio.setText(String.valueOf(materia.getAnio()));
            jTextFieldNombre.setText(String.valueOf(materia.getNombre()));
            jRadioButtonEstado.setSelected(materia.isEstado());
        }catch(NullPointerException n){
            JOptionPane.showMessageDialog(this, "Valor de ID invalido o inexistente");

        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed


